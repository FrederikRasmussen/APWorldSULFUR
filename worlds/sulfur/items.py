class ItemDetails:
    def __init__(
            self,
            id: int,
            name: str,
            default_classification: ItemClassification,
            tags: List[str],
    ):
        self.id: int = id
        self.name: str = name
        self.default_classification: ItemClassification = default_classification
        self.tags: List[str] = tags

    def should_generate(self):
        if self.default_classification is None:
            return False
        if ItemTags.do_not_generate in self.tags:
            return False
        if ItemTags.unknown in self.tags:
            return False
        return True

def item_name_to_id() -> dict[str, int]:
    return_dict: dict[str, int] = {}
    for details in ITEMS:
        return_dict[details.name] = details.id
    return return_dict

def item_identifier_to_details() -> dict[str, ItemDetails]:
    return_dict: dict[str, ItemDetails] = {}
    for details in ITEMS:
        if details.name in return_dict:
            print(details.name + " already in dict")
        return_dict[details.name] = details
    return return_dict

TAG_TO_ITEMS: dict[str, List[ItemDetails]] = {}
def initialise_tag_to_items_dict():
    for details in ITEMS:
        for tag in details.tags:
            if not tag in TAG_TO_ITEMS:
                TAG_TO_ITEMS[tag] = []
            TAG_TO_ITEMS[tag].append(details)

def get_item_names_from_tag(tag: str) -> set[str]:
    return set(map(lambda details: details.name, TAG_TO_ITEMS[tag]))

